# Dense class refers to a fully connecteurald ne network layer where every neuron receives input from all neurons of the previous layer
class Dense:
  def __init__(self, in_dim: int, out_dim: int):
    self.in_dim = in_dim
    self.out_dim = out_dim

    # Khoi tao trong so [-limit, limit]
    limit = math.sqrt(6 / (in_dim + out_dim))
    self.W = [[random.uniform(-limit, limit) for _ in range(out_dim)] for _ in range(in_dim)]
    self.b = [[0.0] for _ in range(out_dim)] # bias
    self.X_cache = None # input dung trong backward

    #Gradient
    self.dW = zeros((in_dim, out_dim))
    self.db = [[0.0 for _ in range(out_dim)]]

  def foward(self, X):
    # Z = X*W + b
    Z = mat_dot(X, self.W)
    m, n = shape(Z)
    for i in range(m):
      for j in range(self.out_dim):
        Z[i][j] += self.b[0][j]
    self.X_cache = X
    return Z

  def backward(self, dZ):
    # dz: gradient tang sau
    X = self.X_cache
    m, n = shape(X)
    # dW = X^T * dZ / m
    Xt = mat_transpose(X)
    dW_full = mat_dot(Xt, dZ)
    #db = tong hang cua dZ / m
    self.dW = scalar_mul_mat(1.0/m, dW_full)
    self.db = scalar_mul_mat(1.0/m, mat_sum_rows(dZ))
    # dA_prev = dZ * W^T
    Wt = mat_transpose(self.W)
    dA_prev = mat_dot(dZ, Wt)
    return dA_prev

  def step(self, lr:float):
    for i in range(self.in_dim):
      for j in range(self.out_dim):
        self.W[i][j] -= lr * self.dW[i][j]

    for j in range(self.out_dim):
      self.b[0][j] -= lr * self.db[0][j]

