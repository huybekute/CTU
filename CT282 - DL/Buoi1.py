class Sigmoid:
  def __init__ (self):
    self.A_cache = None

  def foward(self, X):
    A = mat_apply(X, lambda z: 1.0/ (1.0+math.exp(-z)))
    self.A_cache = A
    return A

  def backward(self, dA):
    m, n = shape(dA)
    dZ = zeros((m, n))
    for i in range(m):
      for j in range(n):
        a = self.A_cache[i][j]
        dZ[i][j] = dA[i][j] * a * (1-a)
    return dZ

