# Dropout layer

class Dropout:
  def __init__(self, p = 0.5):
    self.p = p
    self.mask = None
    self.training = True

  def foward(self, X):
    if not self.training:
      return X
    m, n = shape(X)
    self.mask = [[(random.random() < self.p) for _ in range(n)] for _ in range(m)]
    return [[X[i][j] * self.mask[i][j] / self.p for j in range(n)] for i in range(m)]

  def backward(self, dA):
    if not self.training:
      return dA

    m, n  = shape(dA)
    return [[dA[i][j] * self.mask[i][j] / self.p for j in range(n)] for i in range(m)]

