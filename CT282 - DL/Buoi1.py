# Tao ma tran toan so 0
def zeros(shape: Tuple[int, int]) -> List[List[float]]:
  return [[0.0 for _ in range(shape[1])] for _ in range(shape[0])]

# Lay kich thuoc cua ma tran
def shape(mat: List[List[float]]) -> Tuple[int, int]:
  if mat == []:
    return 0
  else:
    return (len(mat), len(mat[0]))

# Cong 2 ma tran cung kich thuoc
def mat_add(A, B):
  m, n = shape(A)
  total = zeros((m, n))
  for i in range(m):
    for j in range(n):
      total[i][j] = A[i][j] + B[i][j]
  return total

# Tru 2 ma tran cung kich thuoc
def mat_sub(A, B):
  m, n = shape(A)
  total = zeros((m, n))
  for i in range(m):
    for j in range(n):
      total[i][j] = A[i][j] - B[i][j]
  return total

# Nhan ma tran voi 1 so vo huong s
def scalar_mul_mat(s, A):
  m, n = shape(A)
  for i in range(m):
    for j in range(n):
      A[i][j] *= s
  return A

# Chuyen vi ma tran
def mat_transpose(A):
  m, n = shape(A)
  B = zeros((n, m))
  for i in range(m):
    for j in range(n):
      B[j][i] = A[i][j]
  return B

# Nhan ma tran
def mat_dot(A, B):
  m, n = shape(A)
  nb, x = shape(B)
  assert n == nb
  C = zeros((m, x))
  for i in range(m):
    Ai = A[i]
    Ci = C[i]
    for t in range(n):
      At = Ai[t]
      Bt = B[t]
      for j in range(x):
        Ci[j] += At * Bt[j]
  return C
# Tinh tong theop tung cot
def mat_sum_rows(A):
  m, n = shape(A)
  s = [0.0]*n
  for i in range(m):
    for j in range(n):
      s[j] += A[i][j]
  return [s]

# Ap dung ham f len tung phan tu cua ma tran
def mat_apply(A, f):
  m, n = shape(A)
  for i in range(m):
    for j in range(n):
      A[i][j] = f(A[i][j])
  return A



